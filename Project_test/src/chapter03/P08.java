package chapter03;

public class P08 {

	public static void main(String[] args) {
		int a = 10;
		
		int b = 5;
		
		int result = ~a;
		int result1 = a & b;
		int result2 = a | b;
		int result3 = a ^ b;
		
		System.out.println(result);
		System.out.println(result1);
		System.out.println(result2);
		System.out.println(result3);
		

	}

}
